            }

            /* cart render */
            function render() {

                let html = "";
                let subtotal = 0;

                for (let i in cart) {
                    let t = cart[i].price * cart[i].qty;
                    subtotal += t;


                    let lineBtn = document.getElementById("lineBtn");

                    if (subtotal === 0) {
                        lineBtn.disabled = true;
                        lineBtn.style.opacity = "0.5";
                        lineBtn.style.cursor = "not-allowed";
                    } else {
                        lineBtn.disabled = false;
                        lineBtn.style.opacity = "1";
                        lineBtn.style.cursor = "pointer";
                    }

                    
                    html += `<div class="cart-item">
        <div>${i}<br>¥${cart[i].price} × ${cart[i].qty}</div>
        <div>
        <button class="qty-btn" onclick="minus('${i}')">-</button>
        <button class="qty-btn" onclick="plus('${i}')">+</button>
        <button class="delete-btn" onclick="del('${i}')">x</button>
        </div></div>`;
                }




                // GET ORDER TYPE CORRECTLY
                let type = document.querySelector('input[name="ordertype"]:checked').value;

                let delivery = 0;
                subtotal = Number(subtotal);
                if (type === "Delivery" && subtotal > 0 && subtotal < 2000) {
                    delivery = 300;
                }


                let finalTotal = subtotal + delivery;

                document.getElementById("cartItems").innerHTML = html;
                document.getElementById("subtotal").innerText = subtotal;
                document.getElementById("deliveryAmount").innerText = delivery;
                document.getElementById("total").innerText = finalTotal;


                if (delivery > 0) {
                    document.getElementById("deliveryFee").innerText =
                        "※2,000円未満は配達料300円";
                } else {
                    document.getElementById("deliveryFee").innerText = "";
                }
            }

            function plus(n) {
                cart[n].qty++;
                render();
            }

            function minus(n) {
                cart[n].qty--;
                if (cart[n].qty <= 0) delete cart[n];
                render();
            }

            function del(n) {
                delete cart[n];
                render();
            }

